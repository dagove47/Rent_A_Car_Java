    private void typeUserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_typeUserMousePressed
        // TODO add your handling code here:
        if (typeUser.getText().equals("Ingrese su username")) {
            typeUser.setText("");
            typeUser.setForeground(Color.black);
        }
        if (String.valueOf(typePass.getPassword()).isEmpty()) {
            typePass.setText("**********");
            typePass.setForeground(Color.gray);
        }
    }//GEN-LAST:event_typeUserMousePressed

    private void typePassMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_typePassMousePressed
        if (typeUser.getText().isEmpty()) {
            typeUser.setText("Ingrese su username");
            typeUser.setForeground(Color.gray);
        }
        if (String.valueOf(typePass.getPassword()).equals("**********")) {
            typePass.setText("");
            typePass.setForeground(Color.black);
        }
    }//GEN-LAST:event_typePassMousePressed

